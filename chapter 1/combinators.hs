Combinators Determine if each of the following are combinators
or not.
1. λx.xxx
Ans: Yes they are combinators. They use only same variable introduced in lambda and no free variables exist.

2. λxy.zx
Ans: No they are not combinators. It has free variables.

3. λxyz.xy(zx)
Ans: Yes

4. λxyz.xy(zxy)
Ans: Yes 

5. λxy.xy(zxy)
Ans: No. z is a free variable and was not introduced in the lambda head

