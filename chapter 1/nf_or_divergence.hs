Normal form or diverge? Determine if each of the following can
be reduced to a normal form or if they diverge.
1. λx.xxx
Ans:Normal Form(NF). no more reduction steps

2. (λz.zz)(λy.yy)
Ans:Diverges. They both are Alpha equivalent. so (λz.zz) can be (λy.yy) and we get (λy.yy)(λy.yy) which form infinite loop

3. (λx.xxx)z
Ans:Nf as the function is reduced to zzz
